function generateClasses(): SchoolClass[] {
  const classes: SchoolClass[] = [];
  const layerConfigs = [
    { id: 'layer-z', name: 'ז', count: 7 },
    { id: 'layer-h', name: 'ח', count: 7 },
    { id: 'layer-t', name: 'ט', count: 7 },
    { id: 'layer-y', name: 'י', count: 8 },
    { id: 'layer-ya', name: 'יא', count: 8 },
    { id: 'layer-yb', name: 'יב', count: 8 },
  ];

  layerConfigs.forEach(({ id, name, count }) => {
    for (let i = 1; i <= count; i++) {
      classes.push({
        id: `${id}-${i}`,
        layerId: id,
        displayName: `${name}${i}`,
        sortOrder: i,
        isActive: true,
      });
    }
  });

  return classes;
}

